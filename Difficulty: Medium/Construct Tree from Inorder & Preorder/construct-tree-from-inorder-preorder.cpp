class Solution {
  public:
  Node* buildTreeHelper(vector<int>& preorder, int preStart, int preEnd, 
                              vector<int>& inorder, int inStart, int inEnd, 
                              unordered_map<int, int>& inorderMap) {
        if (preStart > preEnd || inStart > inEnd) {
            return nullptr;
        }
        
        int rootVal = preorder[preStart];
        Node* root = new Node(rootVal);
        
        int inRootIndex = inorderMap[rootVal];
        
        int numLeft = inRootIndex - inStart;
        
        root->left = buildTreeHelper(preorder, preStart + 1, preStart + numLeft, 
                                     inorder, inStart, inRootIndex - 1, inorderMap);
        
        root->right = buildTreeHelper(preorder, preStart + numLeft + 1, preEnd, 
                                      inorder, inRootIndex + 1, inEnd, inorderMap);
        
        return root;
    }
    Node *buildTree(vector<int> &inorder, vector<int> &preorder) {
        unordered_map<int, int> inorderMap;
        for (int i = 0; i < inorder.size(); ++i) {
            inorderMap[inorder[i]] = i;
        }
        
        return buildTreeHelper(preorder, 0, preorder.size() - 1, inorder, 0, inorder.size() - 1, inorderMap);
    }
};

